ok = non = bad = 0

for msg, sq, keyword, min_score in CASES:
    result = retrieve(sq, TENANT, "th", top_k=3)
    top = result.documents[0] if result.documents else None

    if not top:
        bad += 1
        print(f"BAD  [{msg!r}]\n     → no results  sq={sq!r}")
        continue

    found_keyword = keyword.lower() in top.question.lower()

    if found_keyword and top.score >= min_score:
        ok += 1
        print(f"OK   [{msg!r}]\n     score={top.score:.3f}  Q={top.question[:55]}")
    elif found_keyword:
        non += 1
        print(f"NON  [{msg!r}]\n     score={top.score:.3f} < {min_score}  Q={top.question[:55]}")
    else:
        any_found = any(keyword.lower() in d.question.lower() for d in result.documents)
        if any_found:
            rank = next(i+1 for i, d in enumerate(result.documents) if keyword.lower() in d.question.lower())
            non += 1
            print(f"NON  [{msg!r}]\n     found at rank #{rank}  #1={top.question[:45]}")
        else:
            bad += 1
            print(f"BAD  [{msg!r}]\n     #1={top.question[:45]}  score={top.score:.3f}  sq={sq!r}")

total = ok + non + bad
print(f"\n{'='*60}")
print(f"Results: {ok} OK / {non} NON / {bad} BAD  (total {total})")
print(f"Score:   {ok}/{total} = {ok/total*100:.0f}%")
