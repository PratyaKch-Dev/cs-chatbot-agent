print("─" * 90)

ok = bad = 0
bad_list = []

for i, (msg, expect_keyword) in enumerate(CASES):
    uid = f"e2e_{_RUN_ID}_{i}"
    t0 = time.perf_counter()
    result = handle_message(tenant_id=TENANT, user_id=uid, message=msg)
    ms = int((time.perf_counter() - t0) * 1000)

    found = expect_keyword.lower() in result.answer.lower()
    escalated = result.was_escalated
    status = "OK" if found and not escalated else "BAD"

    if status == "OK":
        ok += 1
    else:
        bad += 1
        bad_list.append((msg, expect_keyword, result.answer[:120]))

    esc_mark = "ESC" if escalated else "   "
    print(f"{status:<6} {ms:<6} {esc_mark:<5} {expect_keyword:<18} {msg[:50]}")

total = ok + bad
print(f"\n{'='*70}")
print(f"Results: {ok} OK / {bad} BAD  (total {total})")

if bad_list:
    print(f"\nBAD cases:")
    for msg, kw, ans in bad_list:
        print(f"  [{kw}] {msg!r}")
        print(f"  Answer: {ans}")
        print()
